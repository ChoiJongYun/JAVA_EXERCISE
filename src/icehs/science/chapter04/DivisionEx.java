package icehs.science.chapter04;

public class DivisionEx {

	public static void main(String[] args) {
		int number1 =20;
		int number2 =7;
		
		int plusResult = number1+number2;
		int subtractionResult = number1-number2;
		int multiplicationResult = number1*number2;
		int divisionResult= number1/number2;
		int remainResult= number1%number2;
		
		System.out.println("덧셈 결과:"+ plusResult);
		System.out.println("빼기 결과:"+ subtractionResult);
		System.out.println("곱하기 결과:"+ multiplicationResult);
		System.out.println("나누기 결과:"+ divisionResult);
		System.out.println("나머지:"+ remainResult);
		

	}

}
