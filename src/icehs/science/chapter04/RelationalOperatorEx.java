package icehs.science.chapter04;

public class RelationalOperatorEx {

	public static void main(String[] args) {
		
		
		int n1= 10;
		int n2= 3;
		int n3= 3;
		
		System.out.println(n1 >n2);
		System.out.println(n1 <n2);
		System.out.println(n1 >=n2);
		System.out.println(n1 <=n2);
		System.out.println(n2 >=n3);
		System.out.println(n1 ==n2);
		System.out.println(n2 == n3);
		System.out.println(n1 != n2);
		System.out.println(n2 != n3);
	    

	}

}
